    public String validateName(String strInput) {
       
        Pattern ptnName = Pattern.compile("[0-9]");
        Matcher mtcName = ptnName.matcher(strInput);
        if(mtcName.matches()) {
            return "Name format is invalid!";
        }
        return "";
    }
    
    private String validateSpeciesName(String strInput) {
        
        if(strInput.equals("")) {
            return "Species name cannot be null";
        } else if(!strInput.equals("")) {
            return validateName(strInput);
        }
        return "";
    }
    
