    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(validateSpeciesName(txtSpeciesName.getText().trim()).equals("")) {
            
            if(Client.currentInstance().contactServer(new String[]{"SELECT * FROM Species WHERE SpeciesName ='"+
                    txtSpeciesName.getText().trim()+"'", "Check"})[0].equals("true")) { // Check if species is already added
                
                JOptionPane.showMessageDialog(this, "The species exist in the database", "WARNING", JOptionPane.WARNING_MESSAGE);
                txtSpeciesName.requestFocusInWindow();
                
            } else {
                // Send data to the server to add species and check if the output
                // recieved from the server confirms the species is added
                if(Client.currentInstance().contactServer(new String[]{"INSERT INTO Species (SpeciesName)VALUES('"+
                    txtSpeciesName.getText().trim()+"')", "Execute"})[0].equals("true")) { 
                    
                    JOptionPane.showMessageDialog(this, "Species added.", "MESSAGE", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, validateSpeciesName(txtSpeciesName.getText().trim()), 
                    "WARNING", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(btnUpdate.getText().equals("Update")) {
            
            new ChooseRecord("SELECT SpeciesName FROM Species").setVisible(true);
            if(ChooseRecord.getOption() != null) {
                String[] arrSpeciesDetails = Client.currentInstance().contactServer(
                    new String[]{"SELECT SpeciesID, SpeciesName FROM Species WHERE "
                        + "SpeciesName ='"+ChooseRecord.getOption()+"'", "Row"});
            
                intSpeciesID = Integer.parseInt(arrSpeciesDetails[0]);
                txtSpeciesName.setText(arrSpeciesDetails[1]);
                btnUpdate.setText("Save..");
            }
            
            
            
        } else if(btnUpdate.getText().equals("Save..")) {
            if(validateSpeciesName(txtSpeciesName.getText()).equals("")) {
                // Checks if species is already in the database and it is not the same as the value that was initially
                // extracted from the database
                if(Client.currentInstance().contactServer(new String[]{"SELECT * FROM Species WHERE SpeciesName ='"+
                        txtSpeciesName.getText().trim()+"' AND SpeciesID <> "+intSpeciesID, "Check"})[0].equals("true")) { 
                
                    JOptionPane.showMessageDialog(this, "The species exist in the database", "WARNING", JOptionPane.WARNING_MESSAGE);
                    txtSpeciesName.requestFocusInWindow();
                    btnUpdate.setText("Update");
                
                } else {
                    if(Client.currentInstance().contactServer(
                        new String[]{"UPDATE Species SET SpeciesName ='"+
                                txtSpeciesName.getText().trim() + "' WHERE SpeciesID ="+
                                intSpeciesID, "Execute"})[0].equals("true")) {
                
                        JOptionPane.showMessageDialog(this, "Update successful", "MESSAGE", JOptionPane.INFORMATION_MESSAGE);
                        btnUpdate.setText("Update");
                    }
                }
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

